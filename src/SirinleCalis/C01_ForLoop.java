package SirinleCalis;

public class C01_ForLoop {
    public static void main(String[] args) {
        /*10 ile 30 arasindaki sayilari aralarinda virgul koyup ayni sati
        rda yazdirin (10 ve 30 dahil)
         */
        for (int i = 10; i <30 ; i++) {
            System.out.print(i+",");

        }
        System.out.print(30);
    }

}
